public class CalkExperement {

    static int add(int a, int b){
        return a+b;
    }
    static int sub(int a, int b){
        return a-b;
    }
    static int mul(int a, int b){
        return a*b;
    }
    static double div(int a, int b){
        return a/(double)b;
    }
}
